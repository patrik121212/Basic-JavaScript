
// | Operator  | Fungsi                      |
// | --------- | --------------------------- |
// | `=`       | Mengisi nilai               |
// | `==`      | Bandingkan nilai saja       |
// | `===`     | Bandingkan nilai + tipe     |
// | `!=`      | Tidak sama (nilai)          |
// | `!==`     | Tidak sama (nilai + tipe)   |
// | `>` `<`   | Lebih besar / kecil         |
// | `>=` `<=` | Lebih besar/kecil atau sama |
 
let a = 5
let b = "string"
console.log(a == b) // output: false karena nilai berbeda

let x = 10
let y = "20"
console.log(x === y) // output: false karena tipe berbeda