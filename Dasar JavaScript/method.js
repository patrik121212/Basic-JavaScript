// Method adalah fungsi yang dimiliki oleh suatu tipe data
// contoh penggunaan method dalama javascript
// method pada tipe data string
let nama = "Patrik Tangdilintin"
console.log(nama.toUpperCase()) // output: JOHN DOE karena method toUpperCase mengubah semua karakter menjadi huruf besar
console.log(nama.toLowerCase()) // output: john doe karena method toLowerCase mengubah semua karakter menjadi huruf kecil
console.log(nama.length) // output: 8 karena method length menghitung jumlah karakter pada string
console.log(nama.trim()) // output: "John Doe" karena method trim menghapus spasi di awal dan akhir string

// Method dan argumen (parameter)
let kalimat = "Saya belajar JavaScript"
console.log(kalimat.replace("JavaScript", "TypeScript")) // output: Saya belajar TypeScript karena method replace mengganti kata JavaScript dengan TypeScript

